// Las clases inician con mayúscula y las variables con minúscula
// Al final de cada sentencia debemos agregar ;
public class CaracteresEspeciales {
    public static void main(String args[]){
        
     var nombre="José";
     
     System.out.println("\nNueva línea:\n"+nombre); //Salto de línea es con antidiagonal + n (\n)
     System.out.println("Tabulador:\t"+nombre); //Tabular es con antidiagonal + t (\t)
     System.out.println("Retroceso: \b"+nombre+"\n"); //Para retroceso es antidiagonal +b (\b)
     System.out.println("Comilla simple: \'"+nombre+"\'"); //Imprimir el nombre en comillas simples
     System.out.println("Comillas dobles: \""+nombre+"\"\n"); //Imprimir comillas dobles
                 //Video 13

    }
    
}
